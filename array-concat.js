function arrayConcat() {
    var negara = ['indonesia', 'malaysia', 'jepang']
    var ibukota = ['jakarta', 'kuala lumpur' , 'tokyo']

    var hasil = negara.concat(ibukota)
    return hasil
}
console.log(arrayConcat())


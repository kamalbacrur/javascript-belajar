function belajarSwitch () {
    var pilihan = 2;

    switch(pilihan) {
        case 10: 
        console.log("sangat bagus");
        break;
        case 8:
        console.log("bagus");
        break;
        case 6: 
        console.log("cukup");
        break;
        default:
        console.log("lainnya");
    }
}
belajarSwitch();